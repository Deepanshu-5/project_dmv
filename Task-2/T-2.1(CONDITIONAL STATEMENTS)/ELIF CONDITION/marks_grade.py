print("this is student grade calculator")
marks = float(input("enter your marks: "))
if marks >= 90:
    print("you got A grade")
elif marks >= 80:
    print("you got B grade")
elif marks >= 70:
    print("you got C grade")
elif marks >= 60:
    print("you got D grade")
elif marks >= 50:
    print("you got E grade")
else:
    print("you got F grade")